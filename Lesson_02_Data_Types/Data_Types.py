# List Data Type
name_list = ['Jon', '25tc', 'asd']
print(name_list)

name1, name2, name3 = name_list
print(name1, name2, name3)

# Tuple Data Type
name_tuple = ('Jon', '25tu', 'abcd')
print(type(name_tuple))

# Dictionary Data Type
name_dict = {"name": "Jon", "age": 30, "city": "Toronto"}
print(type(name_dict))

#Boolean Data Type
name_boolean = True
print(type(name_boolean))

#Range Data Type
name_range = range(10)
print(type(name_range))

#Bytes Data Type
name_bytes = b"Mirza"
print(name_bytes)
