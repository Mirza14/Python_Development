# Write a function that takes a list as an argument and returns the length of the list

cities = ['New York', 'Los Angeles', 'Chicago', 'Houston', 'Phoenix', 'Philadelphia', 'San Antonio', 'San Diego', 'Dallas', 'San Jose']
contries = ['USA', 'Canada', 'Mexico', 'Brazil', 'Argentina', 'Chile', 'Peru', 'Colombia', 'Venezuela', 'Uruguay']

def list_length(list): #function definition; parameters
    print(len(list))
    return len(list)

list_length(cities) #function call; arguments
list_length(contries) #function call; arguments