# Problem: Write a program to add 2 numbers in Python

# Solution 1 with predefined variables

num1 = 5
num2 = 10

sum = num1 + num2
print("The sum of given two variables is: ", sum)

# Solution 2 with User inputs

user_1 = float(input("Enter your 1st number: "))
user_2 = float(input("Enter your 2nd number: "))

sum_of_2 = user_1 + user_2
print("The sum of user input values is: ", sum_of_2)